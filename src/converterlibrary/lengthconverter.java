package converterlibrary;

public class lengthconverter {
	public static double metertoKM(double meter)
	{
		double KM;
		
			KM=meter/1000;
		return KM;
	}
	
	public static double KMtometer(double KM)
	{
		double meter;
		
			meter=KM*1000;
		return meter;
	}	

}

