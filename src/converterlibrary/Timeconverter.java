package converterlibrary;
 public class Timeconverter{

public static double Hour_To_Minute(double Hour)
{
double Minute;
Minute=Hour*60;
return Minute;

}
public static double Minute_To_Hour(double Minute)
{
double Hour;
Hour=Minute/60;
return Hour;

}
public static double Hour_To_Seconds(double Hour)
{
double Seconds;
Seconds=Hour*3600;
return Seconds;

}
public static double Seconds_To_Hour(double Seconds)
{
double Hour;
Hour=Seconds/3600;
return Hour;

}

}

	
	
	
